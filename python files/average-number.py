# Finding an average can be a useful way to understand the underlying trends of some data.
# Write an average() function that takes in two parameters: num1, num2
def average(num1, num2):
# It should calculate and return the average of the two numbers.
    return (num1 + num2)/2
# The average of num1 and num2 is: (num1 + num2)/2
avg = average(2000, 2000)
print(avg)
